class User {
  final int id;
  final String name;
  final String surname;
  final String role;
  final String position;
  final String login;
  final String email;
  final String phone;

  User({
    required this.id,
    required this.name,
    required this.surname,
    required this.role,
    required this.position,
    required this.login,
    required this.email,
    required this.phone,
  });
}